public class escapeSequences {
    public static void main(String[] args) {

        System.out.println("Tab\tSpace"); // Prints a tab space
        System.out.println("Newline\nNext Line"); // Prints a newline
        System.out.println("Nguyen\rHuy"); //return
        System.out.println("Nguyen\bHuy"); // Prints backspace
        System.out.println("Nguyen\fHuy"); 

    }
}
