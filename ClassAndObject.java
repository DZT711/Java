

public class ClassAndObject {
    // Attributes (fields)
    String name;
    int age;

    // Constructor
    public ClassAndObject(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Method to display information
    public void displayInfo() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
    }


}
